-- Creates token session table with expiry and revocation tracking.
CREATE TABLE user_sessions (
    session_id         NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id            NUMBER NOT NULL,
    token_hash         VARCHAR2(64 CHAR) NOT NULL,
    issued_at          TIMESTAMP NOT NULL,
    expires_at         TIMESTAMP NOT NULL,
    last_accessed_at   TIMESTAMP NOT NULL,
    is_revoked         NUMBER(1) DEFAULT 0 NOT NULL,
    CONSTRAINT fk_user_sessions_user FOREIGN KEY (user_id) REFERENCES app_users(user_id)
);

ALTER TABLE user_sessions
    ADD CONSTRAINT uk_user_sessions_token_hash UNIQUE (token_hash);
